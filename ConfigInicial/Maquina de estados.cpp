// Moves/alters the camera positions based on user input
void DoMovement()
{

	// Camera controls
	if (keys[GLFW_KEY_W] || keys[GLFW_KEY_UP])
	{
		camera.ProcessKeyboard(FORWARD, deltaTime);

	}

	if (keys[GLFW_KEY_S] || keys[GLFW_KEY_DOWN])
	{
		camera.ProcessKeyboard(BACKWARD, deltaTime);


	}

	if (keys[GLFW_KEY_A] || keys[GLFW_KEY_LEFT])
	{
		camera.ProcessKeyboard(LEFT, deltaTime);


	}

	if (keys[GLFW_KEY_D] || keys[GLFW_KEY_RIGHT])
	{
		camera.ProcessKeyboard(RIGHT, deltaTime);


	}

	if (keys[GLFW_KEY_T])
	{
		pointLightPositions[0].x += 0.01f;
	}
	if (keys[GLFW_KEY_G])
	{
		pointLightPositions[0].x -= 0.01f;
	}

	if (keys[GLFW_KEY_Y])
	{
		pointLightPositions[0].y += 0.01f;
	}

	if (keys[GLFW_KEY_H])
	{
		pointLightPositions[0].y -= 0.01f;
	}
	if (keys[GLFW_KEY_U])
	{
		pointLightPositions[0].z -= 0.1f;
	}
	if (keys[GLFW_KEY_J])
	{
		pointLightPositions[0].z += 0.01f;
	}
	
}

// Is called whenever a key is pressed/released via GLFW
void KeyCallback(GLFWwindow *window, int key, int scancode, int action, int mode)
{
	if (GLFW_KEY_ESCAPE == key && GLFW_PRESS == action)
	{
		glfwSetWindowShouldClose(window, GL_TRUE);
	}

	if (key >= 0 && key < 1024)
	{
		if (action == GLFW_PRESS)
		{
			keys[key] = true;
		}
		else if (action == GLFW_RELEASE)
		{
			keys[key] = false;
		}
	}

	if (keys[GLFW_KEY_SPACE])
	{
		active = !active;
		if (active)
		{
			Light1 = glm::vec3(0.2f, 0.8f, 1.0f);
			
		}
		else
		{
			Light1 = glm::vec3(0);//Cuado es solo un valor en los 3 vectores pueden dejar solo una componente
		}
	}
	if (keys[GLFW_KEY_N])
	{
		AnimBall = !AnimBall;
		
	}
	if (keys[GLFW_KEY_B])
	{
		if (dogState == 0) {
			dogState = 1;
		}

	}
	
}

void Animation() {
	if (AnimBall)
	{
		rotBall += 0.4f;
	}

	// --- INICIO DE LA MÁQUINA DE ESTADOS---
	switch (dogState)
	{
	case 0: // Estado 0: IDLE
		ResetLimbs();
		break;

	case 1: // Estado 1: Caminar hacia +Z
		AnimateWalkCycle();
		dogPos.x += WALK_SPEED * sin(glm::radians(dogRot));
		dogPos.z += WALK_SPEED * cos(glm::radians(dogRot));

		// Condición de Transición:
		if (dogPos.z >= BORDER_POS_Z) {
			dogPos.z = BORDER_POS_Z; 
			targetRot = 90.0f;       
			dogState = 2;            
		}
		break;

	case 2: // Estado 2: Girar a 90 grados (hacia +X)
		ResetLimbs(); 
		dogRot += TURN_SPEED; 

		// Condición de Transición:
		if (dogRot >= targetRot) {
			dogRot = targetRot; 
			dogState = 3;       
		}
		break;

	case 3: // Estado 3: Caminar hacia +X
		AnimateWalkCycle();
		dogPos.x += WALK_SPEED * sin(glm::radians(dogRot));
		dogPos.z += WALK_SPEED * cos(glm::radians(dogRot));

		// Condición de Transición:
		if (dogPos.x >= BORDER_POS_X) {
			dogPos.x = BORDER_POS_X; 
			targetRot = 180.0f;      
			dogState = 4;            
		}
		break;

	case 4: // Estado 4: Girar a 180 grados (hacia -Z)
		ResetLimbs();
		dogRot += TURN_SPEED;

		// Condición de Transición:
		if (dogRot >= targetRot) {
			dogRot = targetRot; 
			dogState = 5;       
		}
		break;

	case 5: // Estado 5: Caminar hacia -Z
		AnimateWalkCycle();
		
		dogPos.x += WALK_SPEED * sin(glm::radians(dogRot));
		dogPos.z += WALK_SPEED * cos(glm::radians(dogRot));

		// Condición de Transición:
		if (dogPos.z <= BORDER_NEG_Z) {
			dogPos.z = BORDER_NEG_Z; 
			targetRot = 315.0f;      
			dogState = 6;            
		}
		break;

	case 6: // Estado 6: Girar a 315 grados (Diagonal al centro)
		ResetLimbs();
		dogRot += TURN_SPEED;

		// Condición de Transición:
		if (dogRot >= targetRot) {
			dogRot = targetRot; 
			dogState = 7;       
		}
		break;

	case 7: // Estado 7: Caminar en diagonal al centro
		AnimateWalkCycle();
		dogPos.x += WALK_SPEED * sin(glm::radians(dogRot));
		dogPos.z += WALK_SPEED * cos(glm::radians(dogRot));

		// Condición de Transición
		if (dogPos.x <= CENTER_X) {
			dogPos.x = CENTER_X; 
			dogPos.z = CENTER_Z;
			targetRot = 360.0f;  
			dogState = 8;        
		}
		break;

	case 8: // Estado 8: Girar a 360/0 grados (Posición inicial)
		ResetLimbs();
		dogRot += TURN_SPEED;

		// Condición de Transición:
		if (dogRot >= targetRot) {
			dogRot = 0.0f;  
			dogState = 1;   
		}
		break;
	}
}

void AnimateWalkCycle() {
	if (!step) {
		RLegs += 0.03f;
		FLegs += 0.03f;
		head += 0.03f;
		tail += 0.03f;

		if (RLegs > 15.0f) {
			step = true;
		}
	}
	else {
		RLegs -= 0.03f;
		FLegs -= 0.03f;
		head -= 0.03f;
		tail -= 0.03f;

		if (RLegs < -15.0f) {
			step = false;
		}
	}
}

void ResetLimbs() {
	RLegs = 0.0f;
	FLegs = 0.0f;
	head = 0.0f;
	tail = 0.0f;
}

void MouseCallback(GLFWwindow *window, double xPos, double yPos)
{
	if (firstMouse)
	{
		lastX = xPos;
		lastY = yPos;
		firstMouse = false;
	}

	GLfloat xOffset = xPos - lastX;
	GLfloat yOffset = lastY - yPos;  // Reversed since y-coordinates go from bottom to left

	lastX = xPos;
	lastY = yPos;

	camera.ProcessMouseMovement(xOffset, yOffset);
}